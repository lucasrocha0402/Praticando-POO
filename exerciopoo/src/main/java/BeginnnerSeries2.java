public class BeginnnerSeries2 {
    /*
    O relógio mostra h horas, m minutos e s segundos depois da meia-noite.
    Sua tarefa é escrever uma função que retorne a hora desde a meia-noite em milissegundos.
     */
    public static int Pasta(int h, int m, int s){
        return h * 3600000 + m * 60000 + s * 1000;
    }
}
